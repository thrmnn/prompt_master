class TestOnSectionChanged:
    def test_scores_updated_after_section_change(self, worker):
        worker.on_section_changed(
            "Role",
            "You are an expert data scientist with deep ML knowledge."
        )
        scores = worker.state.section_scores
        # After processing, we should have a score for Role
        assert "Role" in scores

    def test_section_content_stored(self, worker):
        worker.on_section_changed("Task", "Analyze customer churn data.")
        assert worker.state.prompt_sections.get("Task") == "Analyze customer churn data."

    def test_edit_generation_increments(self, worker):
        gen_before = worker.state.edit_generation
        worker.on_section_changed("Role", "Expert analyst.")
        assert worker.state.edit_generation > gen_before


# ---------------------------------------------------------------------------
# IntelligenceWorker — dwell queues whisper
# ---------------------------------------------------------------------------


class TestOnDwell:
    def test_dwell_on_weak_section_queues_whisper(self, worker):
        # Set up a section with a weak score
        worker.on_section_changed("Role", "You are a helpful assistant.")
        # Drain any whispers generated by on_section_changed
        while worker.get_next_whisper() is not None:
            pass
        # Now trigger the dwell
        worker.on_dwell("Role")
        whisper = worker.get_next_whisper()
        # A score whisper should have been queued for the weak Role section
        assert whisper is not None

    def test_dwell_on_section_references_section(self, worker):
        worker.on_section_changed("Role", "You are a helpful assistant.")
        while worker.get_next_whisper() is not None:
            pass
        worker.on_dwell("Role")
        whisper = worker.get_next_whisper()
        assert whisper is not None
        assert whisper.section == "Role"


# ---------------------------------------------------------------------------
# IntelligenceWorker — deep dwell pre-generates variations
# ---------------------------------------------------------------------------


class TestOnDeepDwell:
    def test_deep_dwell_with_cached_data(self, worker):
        """When variations are already cached, deep_dwell retrieves them."""
        content = "You are a helpful assistant."
        worker.on_section_changed("Role", content)
        # Manually pre-populate the cache so deep_dwell can find them
        cache_key = worker._cache.content_key("Role", content)
        fake_variations = [{"dimension": "tone", "value": "formal", "content": "Formal expert."}]
        worker._cache.put(cache_key, fake_variations)
        # Now trigger deep dwell -- should pull from cache
        worker.on_deep_dwell("Role")
        assert "Role" in worker.state.cached_variations
        assert worker.state.cached_variations["Role"] == fake_variations


