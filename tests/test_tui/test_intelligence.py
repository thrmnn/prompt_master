class TestGetNextWhisperPriority:
    def test_higher_priority_comes_first(self, worker):
        # Directly enqueue whispers at different priorities
        worker.state.pending_whispers.append(
            Whisper(text="low prio", section="Role", priority=0, whisper_type="score")
        )
        worker.state.pending_whispers.append(
            Whisper(text="high prio", section="Task", priority=2, whisper_type="improvement")
        )
        worker.state.pending_whispers.append(
            Whisper(text="mid prio", section="Context", priority=1, whisper_type="score")
        )

        first = worker.get_next_whisper()
        assert first is not None
        assert first.priority == 2
        assert first.text == "high prio"

        second = worker.get_next_whisper()
        assert second is not None
        assert second.priority == 1


class TestGetNextWhisperEmpty:
    def test_returns_none_when_empty(self, worker):
        result = worker.get_next_whisper()
        assert result is None


# ---------------------------------------------------------------------------
# Cached variations
# ---------------------------------------------------------------------------


class TestCachedVariations:
    def test_variations_stored(self, worker):
        """Pre-cached variations appear in state.cached_variations."""
        content = "You are a helpful assistant."
        worker.on_section_changed("Role", content)
        cache_key = worker._cache.content_key("Role", content)
        worker._cache.put(cache_key, [{"dimension": "tone", "value": "formal", "content": "Expert."}])
        worker.on_deep_dwell("Role")
        variations = worker.state.cached_variations.get("Role", [])
        assert isinstance(variations, list)
        assert len(variations) > 0

    def test_variations_retrievable_via_get(self, worker):
        """get_cached_variations returns the stored variations."""
        content = "You are a helpful assistant."
        worker.on_section_changed("Role", content)
        cache_key = worker._cache.content_key("Role", content)
        expected = [{"dimension": "tone", "value": "formal", "content": "Rewritten expert."}]
        worker._cache.put(cache_key, expected)
        worker.on_deep_dwell("Role")
        result = worker.get_cached_variations("Role")
        assert result == expected


